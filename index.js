let numero1 = 1;
let numero2 = 2;

function adicao () {
  console.log(`${numero1} + ${numero2} = ${numero1 + numero2}`)
  console.log(numero1 + ' + ' + numero2 + ' = ' + (numero1 + numero2))
}
adicao()

function subtracao () {
  console.log(`${numero1} - ${numero2} = ${numero1 - numero2}`)
  console.log(numero1 + ' - ' + numero2 + ' = ' + (numero1 - numero2))
}
subtracao()

function multiplicacao () {
  console.log(`${numero1} x ${numero2} = ${numero1 * numero2}`)
  console.log(numero1 + ' x ' + numero2 + ' = ' + (numero1 * numero2))
}
multiplicacao()

function divisao () {
  console.log(`${numero1} : ${numero2} = ${numero1 / numero2}`)
  console.log(numero1 + ' : ' + numero2 + ' = ' + (numero1 / numero2))
}
divisao()





